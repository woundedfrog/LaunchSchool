#What are the benefits of using Object Oriented Programming in Ruby? Think of as many as you can.

#ANSWERs

#1) It allowes us to break up functions of our code and group them into reusable piece that we can easily use and reuse.
#This allows us to use functionality on different objects in multiple areas of our program.  
#2) It simplifies more complex programs into a more structured way. Allowing better management of complex code.
#3) Allows programmers to think more abstractly about the code they write.
#4) Conceptually it's easier as objects are represented by nouns.
#5) We can duplicate funtionality in different areas of the code while keeping our code DRY.
#6) Pre-written code allows for faster application building\
